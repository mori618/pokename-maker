// 文字列から確定的にインデックスを生成する関数（ランダムの排除）
function getDeterminedValue(str, max) {
  let h = 0;
  for (let i = 0; i < str.length; i++) h = Math.imul(31, h) + str.charCodeAt(i) | 0;
  return Math.abs(h) % max;
}

pokemonList.forEach(p => {
  let finalNames = { c: [], u: [], j: [], w: [], n: [] };
  
  const baseName = p.name.replace(/メガ/, "").replace(/\(.*\)/, "");
  
  // ベース辞書に存在するか確認
  if (baseDict[baseName]) {
    const dict = baseDict[baseName];
    // メガやリージョンフォームの場合、修飾を追加して6文字に収める
    let prefix = "";
    if (p.name.includes("メガ")) prefix = "メガ";
    if (p.name.includes("アローラ")) prefix = "アロ";
    if (p.name.includes("ガラル")) prefix = "ガラ";
    if (p.name.includes("ヒスイ")) prefix = "ヒス";
    if (p.name.includes("パルデア")) prefix = "パル";
    
    for (let key in dict) {
      finalNames[key] = dict[key].map(n => {
        if (!prefix) return n;
        let combined = prefix + n;
        if (combined.length > 6) combined = combined.substring(0, 6);
        return combined;
      });
    }
  } else {
    // 辞書にない場合は、決定論的に高品質な組み合わせを生成
    let type = p.type1 || "ノーマル";
    let dict = typePremiumDict[type] || typePremiumDict["ノーマル"];
    
    // 名前の短い版（2〜3文字）
    let shortName = baseName.length > 3 ? baseName.substring(0, 3) : baseName;
    let shortName2 = baseName.substring(0, 2);
    
    // c (cool)
    const cWord1 = dict.c[0];
    const cWord2 = dict.c[1];
    finalNames.c.push((cWord1 + shortName2).substring(0, 6));
    finalNames.c.push((shortName2 + cWord2).substring(0, 6));
    
    // u (cute)
    finalNames.u.push((shortName2 + dict.u[0]).substring(0, 6));
    finalNames.u.push(dict.u[1]);
    
    // j (japanese)
    finalNames.j.push((shortName2 + "まる").substring(0, 6));
    finalNames.j.push(dict.j[0]);
    
    // w (western)
    finalNames.w.push((shortName2 + "ドン").substring(0, 6));
    finalNames.w.push(dict.w[0]);
    
    // n (unique)
    finalNames.n.push((shortName2 + "アニキ").substring(0, 6));
    finalNames.n.push(dict.n[0]);
    
    // メガ等対応
    if (p.name.includes("メガ") || p.name.includes("(")) {
        let pre = p.name.includes("メガ") ? "メガ" : "アロ";
        for (let key in finalNames) {
          finalNames[key] = finalNames[key].map(n => {
              let res = pre + n;
              return res.length > 6 ? res.substring(0, 6) : res;
          });
        }
    }
  }

  // aiNicknames の更新
  // 元のgeneralは保持
  let general = [];
  if (p.aiNicknames && p.aiNicknames.general) {
    general = p.aiNicknames.general;
  } else if (p.nicknames) {
    general = p.nicknames;
  }
  
  p.aiNicknames = {
    general: general,
    cool: finalNames.c,
    cute: finalNames.u,
    japanese: finalNames.j,
    western: finalNames.w,
    unique: finalNames.n
  };
});

