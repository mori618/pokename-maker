  const kataSuffix = getRandom(kataSuffixes, rnd);

  // 1. ひらがなの二つ名
  const adjWa = getRandom(typeAdjWa[type] || typeAdjWa["ノーマル"], rnd);
  const nounWa = getRandom(typeNounsWa[type] || typeNounsWa["ノーマル"], rnd);
  let name1 = `${adjWa}${nounWa}`;
  if (name1.length > 6) name1 = name1.substring(0, 6);

  // 2. カタカナ
  const adjKata = getRandom(typeAdjKata[type] || typeAdjKata["ノーマル"], rnd);
  let name2 = `${adjKata}${kataSuffix}`;
  if (name2.length > 6) name2 = name2.substring(0, 6);

